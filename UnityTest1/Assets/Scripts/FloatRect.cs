
namespace Yk
{
    public class FloatRect
    {
        public float l, b, r, t;

        public FloatRect()
        {
        }

        public FloatRect(float l, float b, float r, float t)
        {
            this.l = l;
            this.b = b;
            this.r = r;
            this.t = t;
        }
    }

}



